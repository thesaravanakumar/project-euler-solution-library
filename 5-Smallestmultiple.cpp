#include <bits/stdc++.h>
using namespace std;
int main()
{
    long long int n = 0;
    bool a = true;
    do
    {
        n++;
        a = true;
        for (int i = 1; i <= 20; i++)
        {
            if (n % i != 0)
            {
                a = false;
                break;
            }
        }
    } while (a == false);
    cout << n;
    return 0;
}
